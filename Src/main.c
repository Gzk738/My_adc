void Printinfo_ADC_Value(void)
{
     Value_Average = 0;
     Value_VREF_CAL = VREFINT_CAL_ADDR;
     HAL_ADC_Start(&hadc1);
     if(HAL_ADC_PollForConversion(&hadc1, 200) == HAL_OK)//等待转换完成
     {
           HAL_Delay(1000);
           HAL_ADCEx_Calibration_Start(&hadc1 ,ADC_SINGLE_ENDED);//AD校准

           //采集10个值作为滤波
           if(HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc1), HAL_ADC_STATE_REG_EOC))
           {
                 for(Value_loop = 0;Value_loop<10;Value_loop++)
               {
                   ADC_Value = HAL_ADC_GetValue(&hadc1); 
                   Value_Average += ADC_Value;
               }
               Value_Real=(float)(ADC_Value*(3.3/4095.));
               printf(LOG_FORMAT,Value_Real,(Value_Average/10));
               HAL_Delay(1000);
           }
     }
}

